CREATE TABLE IF NOT EXISTS users(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(250) NOT NULL,
email VARCHAR(254) NOT NULL UNIQUE,
likes INTEGER,
dislikes INTEGER,
rate DOUBLE PRECISION
);

CREATE INDEX idx_users_rate ON users (rate);

CREATE TABLE IF NOT EXISTS categories(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS locations(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
lat DOUBLE PRECISION NOT NULL,
lon DOUBLE PRECISION NOT NULL
);

CREATE TABLE IF NOT EXISTS events(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
annotation VARCHAR(2000) NOT NULL UNIQUE,
category_id BIGINT NOT NULL,
confirmed_request INTEGER,
created_on TIMESTAMP WITHOUT TIME ZONE,
description VARCHAR(7000),
event_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
initiator_id BIGINT NOT NULL,
locations_id BIGINT NOT NULL,
paid BOOLEAN NOT NULL,
participant_limit INTEGER,
published_on TIMESTAMP WITHOUT TIME ZONE,
request_moderation BOOLEAN,
state VARCHAR(10),
title VARCHAR(120) NOT NULL,
views BIGINT,
likes INTEGER,
dislikes INTEGER,
rate DOUBLE PRECISION,
FOREIGN KEY (category_id) REFERENCES categories(id),
FOREIGN KEY (initiator_id) REFERENCES users(id),
FOREIGN KEY (locations_id) REFERENCES locations(id)
);

CREATE INDEX idx_events_rate ON events (rate);

CREATE TABLE IF NOT EXISTS compilations(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
pinned BOOLEAN NOT NULL,
title VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS compilations_events(
compilation_id BIGINT NOT NULL,
event_id BIGINT NOT NULL,
PRIMARY KEY (compilation_id, event_id),
FOREIGN KEY (compilation_id) REFERENCES compilations(id),
FOREIGN KEY (event_id) REFERENCES events(id)
);

CREATE TABLE IF NOT EXISTS participation_request(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
event_id BIGINT NOT NULL,
requester_id BIGINT NOT NULL,
status VARCHAR(50),
FOREIGN KEY (event_id) REFERENCES events(id),
FOREIGN KEY (requester_id) REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS rating(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
user_id BIGINT NOT NULL,
event_id BIGINT NOT NULL,
likes BOOLEAN NOT NULL
);